package br.ufcg.adsd.minitest.eventscheduler;

public class Client {

    private int clazz;
    private int identifier;

    public Client(int clazz, int identifier) {
        this.clazz = clazz;
        this.identifier = identifier;
    }

    public int getClazz() {
        return clazz;
    }


    public int identifier() {
        return identifier;
    }
}
